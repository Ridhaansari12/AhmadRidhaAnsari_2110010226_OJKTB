    private void BtnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnsimpanActionPerformed
             if (Iddestinasi.getText().equals("") || 
                     Nmdestinasi.getText().equals("") ||  
                     Fasilitas.getText().equals("") || 
                     Harga.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
        } else {
            try {
               int s;
               String sql = "INSERT INTO destinasi VALUES ('"+Iddestinasi.getText()+"',"
                       + " '"+Nmdestinasi.getText()+"',"
                       + " '"+Fasilitas.getText()+"',"
                       + " '"+Harga.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                   JOptionPane.showMessageDialog(null, "Sukses");
                   clear();
                   tampilan();
               }
           } catch (Exception e){
                   System.out.println(e.toString());
           } 
       }
    }//GEN-LAST:event_BtnsimpanActionPerformed

    private void BtneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtneditActionPerformed
 int c = JOptionPane.showConfirmDialog(null, "Ingin Mengubah Data?","Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (Iddestinasi.getText().equals("") || 
                    Nmdestinasi.getText().equals("") ||  
                    Fasilitas.getText().equals("") || 
                    Harga.getText().equals("") ){
                JOptionPane.showMessageDialog(null, "Field Tidak Boleh Kosong");
            } else {
                try {
                    int s;
                    String sql = "UPDATE destinasi SET nm_destinasi = '"
                            +Nmdestinasi.getText()+"',"
                            + " fasilitas = '"+Fasilitas.getText()+"', "
                            + "harga = '"+Harga.getText()
                            +"' WHERE id_destinasi = '"
                            +Iddestinasi.getText()+"' ";
                    st = conn.createStatement();
                    s = st.executeUpdate(sql);
                    if (s == 1){
                        JOptionPane.showMessageDialog(null, "Sukses");
                        clear();
                        tampilan();
                    }
                } catch (Exception e){
                        System.out.println(e.toString());
                } 
            }
        }
    }//GEN-LAST:event_BtneditActionPerformed


    public void tampilan(){
        Object [] baris = {"id_destinasi", "nama_destinasi", "fasilitas","harga"};
        tabMode = new DefaultTableModel(null, baris);
        Tbldestinasi.setModel(tabMode);
        try {
                String sql = "SELECT * FROM destinasi";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                while (rs.next()){
                        String id = rs.getString("id_destinasi");
                        String nd = rs.getString("nm_destinasi");
                        String f = rs.getString("fasilitas");
                        String h = rs.getString("harga");

                        Object [] data = {id,nd,f,h};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    
